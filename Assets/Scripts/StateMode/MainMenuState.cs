        sceneName = SceneName.mainMenuScene;
    }
    public override void UpdateAction() {
        base.UpdateAction();
    }

    public override void OnEnter() {
        base.OnEnter();
    }

    public override void OnExit() {
        base.OnExit();
    }
}
